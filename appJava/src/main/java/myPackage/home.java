        String pas = "";
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/hostelmanagement", "root", "abcd");
            Statement sta = con.createStatement();
            ResultSet res = sta.executeQuery("select * from studentdetails where studentid='" + jTextField2.getText() + "'");
            if (res.next() == false) {
                JOptionPane.showMessageDialog(this, "Invalid User ID or Password");
            } else {
                do {
                    pas = res.getString("studentpassword");
                } while (res.next());

                if (pas.equals(jTextField1.getText())) {
                     studentPanel.studid=jTextField2.getText();
                   this.dispose();
                    studentPanel n = new studentPanel();
                    n.setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(this, "Invalid User ID or Password");
                }

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }


